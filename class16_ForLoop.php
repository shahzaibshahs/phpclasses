<?php
//For Loop 
// Increament

echo "<h4>Increament For While Loop</h4>";


for($a = 1; $a <= 10; $a++){
    echo $a . ") Hello World , This Is For Loop Example </br>";
}

echo "<h4>Increament Decreament Loop</h4>";


for($a = 10; $a >= 1; $a--){
    echo $a . ") Hello World , This Is For Loop Example </br>";
}