<?php
//Do While Loop 
// Increament
echo "<h4>Increament In Do While Loop</h4>";
$a = 1;

do{
    echo $a . ") Hello Php Students </br>";
    $a++;
}
while($a<= 10);

//Decreament

echo "<h4>Decreament In Do While Loop</h4>";
$a = 10;

do{
    echo $a . ") Hello Php Students </br>";
    $a--;
}
while($a>= 1);


?>
